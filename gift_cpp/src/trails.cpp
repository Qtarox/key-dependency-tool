static void conversion(uint8_t alpha[30][16],uint32_t trail_rounds)
{
	#if SIZE == 4
	Permutation(alpha[trail_rounds]);
	#elif SIZE == 8 
	Permutation8(alpha[trail_rounds]);
	#endif
}

static bool sanityCheck(uint8_t alpha[30][16],uint32_t trail_rounds)
{
	uint32_t DDT[16][16] = {0};
	computeDDT(DDT);

	uint8_t state_temp_before[16];
	uint8_t state_temp_after[16];
	for (uint32_t n = 0; n < trail_rounds; n++)
	{
		for (int i = 0; i < 16; i++) state_temp_before[i] = alpha[n][i];
		for (int i = 0; i < 16; i++) state_temp_after[i] = alpha[n+1][i];
		#if SIZE == 4
		invPermutation(state_temp_after);
		for (int i = 0; i < 16; i++){
			if (DDT[state_temp_before[i]][state_temp_after[i]] == 0) return false;
		}
		#elif SIZE == 8
		invPermutation8(state_temp_after);
		for (int i = 0; i < 16; i++){
			if (DDT[state_temp_before[i]>>4][state_temp_after[i]>>4] == 0) return false;
			if (DDT[state_temp_before[i]&0xf][state_temp_after[i]&0xf] == 0) return false;
		}
		#endif
	}
	return true;
}

